package heranca_polimorfismo;

public class ClassesMetodosFinal {
    public static void main(String[] args) {
        /*
         * Palavra chave: final
         * Classe: evita que a classe seja herdada
         * "public final class SavingsAccounts"
         * Método: evita que o método sob seja sobreposto
         * 
         * Dependendo das regras do negócio, às vezes é desejável garantir que uma classe não seja herdada, ou que um método não seja sobreposto.
         * Geralmente convém acrescentar final em métodos sobrepostos, pois sobreposição múltiplas podem ser uma porta de entrada para inconsistências
         * 
         * Performance: atributos de tipo de uma classe final são analisados de forma mais rápida em tempo de execução.
         * Exemplo clássico: String
         */
    }
}
